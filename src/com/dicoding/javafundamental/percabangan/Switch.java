package com.dicoding.javafundamental.percabangan;

public class Switch {
    public static void main(String[] args){
        // declare + assignment
        int input = 3;
        switch (input){

            case 1:
            System.out.println("1");
            break;

            case 2:
            System.out.println("2");
            break;

            case 3:
            System.out.println("3");
            break;

            case 4:
                System.out.println("4");
                break;
            default:
                break;
        }
    }
}
